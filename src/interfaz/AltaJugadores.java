    private boolean validarCamposVacios() {
        if (!"".equals(txtNombre.getText().trim()) && !"".equals(txtAlias.getText().trim()) && !"".equals(txtEdad.getText().trim()))
            return true;
        else
            return false;
    }
    
    private void limpiarCampos() {
        txtNombre.setText("");
        txtAlias.setText("");
        txtEdad.setText("");
    }
    
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombre,alias;
        int edad = 0;
        if (!validarCamposVacios())
           JOptionPane.showMessageDialog(this, "Faltan datos necesarios", "Error", JOptionPane.ERROR_MESSAGE); 
        else {
            nombre = txtNombre.getText().trim().toUpperCase();
            if (sistema.validarAlias(txtAlias.getText().trim().toUpperCase())) {
                alias = txtAlias.getText().trim().toUpperCase();
                try {
                    if(Jugador.validarEdad(txtEdad.getText().trim())) {
                        edad = Integer.parseInt(txtEdad.getText().trim());
                        sistema.registrarJugador(new Jugador(nombre,alias,edad)); 
                        JOptionPane.showMessageDialog(this, "Jugador creado con exito!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                        limpiarCampos();
                        
                    } else
                        JOptionPane.showMessageDialog(this, "La edad no es v√°lida", "Error", JOptionPane.ERROR_MESSAGE);              
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE); 
                }                
            }
            else
               JOptionPane.showMessageDialog(this, "Ya existe un jugador con ese alias", "Error", JOptionPane.ERROR_MESSAGE); 
        }
        
        
                
                
                
        
        
        
        
    }//GEN-LAST:event_btnGuardarActionPerformed

